#cross product functions
one_w_many <- function(one, many) lapply(many, function(x) c(one,x))
 
#------------------------------------------------------------------------------#

"%p%" <- function( a, b ) {
  p = c( sapply(a, function (x) one_w_many(x, b) ) )
  if (is.null(unlist(p))) list() else p}

#------------------------------------------------------------------------------#

go <- function (...) {
  prod = Reduce( '%p%', ...)
  return(prod)}




